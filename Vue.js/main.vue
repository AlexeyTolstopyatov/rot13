<script lang="ts">
import Vue from "vue";

const a = "a".charCodeAt(0),
  z = "z".charCodeAt(0),
  A = "A".charCodeAt(0),
  Z = "Z".charCodeAt(0);

export default Vue.extend({
  data(): { itext: string } {
    return {
      itext: "",
    };
  },
  computed: {
    otext(): string {
      return this.itext
        .split("")
        .map((c: string) => {
          let i: number = c.charCodeAt(0);
          if (a <= i && i <= z) i = ((i - a + 13) % 26) + a;
          else if (A <= i && i <= Z) i = ((i - A + 13) % 26) + A;
          return String.fromCharCode(i);
        })
        .join("");
    },
  },
});
</script>

<template>
    <div class="container">
        <div class="row">
            <div class="col">
                <input class="form-control" v-model="itext" placeholder="Enter text" />
                <input class="form-control" disabled v-model="otext" />
            </div>
        </div>
    </div>
</template>

